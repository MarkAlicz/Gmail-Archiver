	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>
	/// Minimal RFC 2047 encoded-word decoder. Named GmailRfc2047 to avoid MimeKit.Rfc2047.
	/// </summary>
	///
	/// <remarks>	I Volt, 6/30/2026. </remarks>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	internal static class GmailRfc2047
	{
		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Decodes. </summary>
		///
		/// <remarks>	I Volt, 6/30/2026. </remarks>
		///
		/// <param name="input">	The input. </param>
		///
		/// <returns>	A string. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		public static string Decode(string input)
		{
			if (string.IsNullOrEmpty(input) || !input.Contains("=?")) return input;
			var sb = new StringBuilder();
			int i = 0;
			while (i < input.Length)
			{
				int start = input.IndexOf("=?", i, StringComparison.Ordinal);
				if (start < 0) { sb.Append(input[i..]); break; }
				sb.Append(input[i..start]);
				int c1 = input.IndexOf('?', start + 2);
				int c2 = c1 < 0 ? -1 : input.IndexOf('?', c1 + 1);
				int end = c2 < 0 ? -1 : input.IndexOf("?=", c2 + 1, StringComparison.Ordinal);
				if (c1 < 0 || c2 < 0 || end < 0) { sb.Append(input[start..]); break; }

				string charset = input[(start + 2)..c1];
				char enc = char.ToUpperInvariant(input[c1 + 1]);
				string text = input[(c2 + 1)..end];
				try
				{
					Encoding e = Encoding.GetEncoding(charset);
					byte[] bytes = enc == 'B'
						? Convert.FromBase64String(text)
						: GmailQuotedPrintable.DecodeQEncoding(text);
					sb.Append(e.GetString(bytes));
				}
				catch { sb.Append(input[start..(end + 2)]); }
				i = end + 2;
			}
			return sb.ToString();
		}
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Quoted-printable + Q-encoding decoder. </summary>
	///
	/// <remarks>	I Volt, 6/30/2026. </remarks>
	////////////////////////////////////////////////////////////////////////////////////////////////////

	internal static class GmailQuotedPrintable
	{
		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Decodes the given input. </summary>
		///
		/// <remarks>	I Volt, 6/30/2026. </remarks>
		///
		/// <param name="input">	The input. </param>
		///
		/// <returns>	A byte[]. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		public static byte[] Decode(byte[] input)
		{
			var outp = new List<byte>(input.Length);
			for (int i = 0;i < input.Length;i++)
			{
				byte b = input[i];
				if (b == (byte)'=' && i + 2 < input.Length)
				{
					if (input[i + 1] == (byte)'\r' && input[i + 2] == (byte)'\n') { i += 2; continue; }
					if (input[i + 1] == (byte)'\n') { i += 1; continue; }
					if (TryHex(input[i + 1], input[i + 2], out byte val)) { outp.Add(val); i += 2; continue; }
				}
				outp.Add(b);
			}
			return outp.ToArray();
		}

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Decodes a q encoding. </summary>
		///
		/// <remarks>	I Volt, 6/30/2026. </remarks>
		///
		/// <param name="text">	The text. </param>
		///
		/// <returns>	A byte[]. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		public static byte[] DecodeQEncoding(string text) =>
			Decode(Encoding.ASCII.GetBytes(text.Replace('_', ' ')));

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Attempts to hexadecimal. </summary>
		///
		/// <remarks>	I Volt, 6/30/2026. </remarks>
		///
		/// <param name="h">	The height. </param>
		/// <param name="l">	A byte to process. </param>
		/// <param name="v">	[out] A byte to fill in. </param>
		///
		/// <returns>	True if it succeeds, false if it fails. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		private static bool TryHex(byte h, byte l, out byte v)
		{
			v = 0;
			int hi = HexVal(h), lo = HexVal(l);
			if (hi < 0 || lo < 0) return false;
			v = (byte)((hi << 4) | lo); return true;
		}

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>	Hexadecimal value. </summary>
		///
		/// <remarks>	I Volt, 6/30/2026. </remarks>
		///
		/// <param name="b">	The byte value. </param>
		///
		/// <returns>	An int. </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		private static int HexVal(byte b) => b switch
		{
			>= (byte)'0' and <= (byte)'9' => b - '0',
			>= (byte)'A' and <= (byte)'F' => b - 'A' + 10,
			>= (byte)'a' and <= (byte)'f' => b - 'a' + 10,
			_ => -1
		};
	}
