		/// <summary>
		/// Returns a list of human-readable problems that would stop the archiver from working. Empty
		/// list means the configuration is usable.
		/// </summary>
		///
		/// <remarks>	I Volt, 7/3/2026. </remarks>
		///
		/// <returns>	The validation problems (empty when valid). </returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////

		public System.Collections.Generic.List<string> Validate()
		{
			var problems = new System.Collections.Generic.List<string>();

			if (string.IsNullOrWhiteSpace(email))
				problems.Add("Email address is missing.");
			else if (!email.Contains('@') || !email.Contains('.'))
				problems.Add($"Email address '{email}' does not look valid.");

			bool haveSecret = !string.IsNullOrWhiteSpace(password)
							  || !string.IsNullOrWhiteSpace(server?.application_specific_password);
			if (!haveSecret)
				problems.Add("No password / app-specific password set (Gmail requires a 16-char app password).");

			if (string.IsNullOrWhiteSpace(archive_folder))
				problems.Add("archive_folder is not set.");
			if (string.IsNullOrWhiteSpace(archive_index_folder))
				problems.Add("archive_index_folder is not set.");

			if ((server?.port ?? 993) <= 0)
				problems.Add("IMAP port is invalid.");

			return problems;
		}

